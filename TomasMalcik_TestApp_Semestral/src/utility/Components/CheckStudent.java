    /**
     * This method checks with regex if the inputs are filled correctly
     * 
     */
    private void checkStudent(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkStudent
        //regex for name and student number (MXXXXXXXX)
        String regName = "[a-zA-Z]+[,\\s][a-zA-Z]+";
        String regNumber = "[a-zA-Z]?\\d{8}";
        if(Pattern.matches(regName, jTextField1.getText())) {
            this.jLabel4.setText("");
            if(Pattern.matches(regNumber, jTextField2.getText())) {
                this.jLabel4.setText("");
                students = FileSystem.load(route);
                verifyStudent(jTextField1.getText(),jTextField2.getText());
            }else {
                this.jLabel5.setText("Vaše studentské číslo musí být ve správném tvaru!");
            }
        }else{
            this.jLabel4.setText("Vaše jméno může obsahovat pouze písmena !");
        }
    }//GEN-LAST:event_checkStudent

