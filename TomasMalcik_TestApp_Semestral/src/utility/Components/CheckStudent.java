    /**
     * This function verifies if the student is included in the list or not.
     * @param name - name of student
     * @param number - his/her student number
     */
    private void verifyStudent(String name, String number) {
         for (int i = 0; i < students.size(); i++) {
             String[] arr = students.get(i).split("#");
            if(name.equalsIgnoreCase(arr[0])) {
                this.jLabel6.setText("");
                if(number.equalsIgnoreCase(arr[1])) {
                    this.jLabel6.setText("");
                    Window.getInstance().changePanel(new Test(jTextField2.getText()));
                }else {
                    this.jLabel6.setText("Vaše údaje nejsou správné");
                }
            }else {
                this.jLabel6.setText("Vaše údaje nejsou správné");
            }
        }
    }

